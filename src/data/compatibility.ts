function getCompatInfo(a: string, b: string): CompatInfo {
  if (a === b) return { group: 'good', label: '동류 궁합', score: 75 };
  const info = COMPAT_MAP[a]?.[b];
  if (info) return info;
  // 매핑에 없으면 기능 유사도로 대략 추정
  const shared = [...a].filter((c, i) => b[i] === c).length;
  if (shared >= 3) return { group: 'good', label: '좋은 궁합', score: 70 };
  if (shared >= 2) return { group: 'neutral', label: '보통 궁합', score: 60 };
  return { group: 'neutral', label: '흥미로운 궁합', score: 55 };
}

