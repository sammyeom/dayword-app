function getDateSeed(dateStr: string): number {
  let hash = 0;
  for (let i = 0; i < dateStr.length; i++) {
    const char = dateStr.charCodeAt(i);
    hash = ((hash << 5) - hash + char) | 0;
  }
  return Math.abs(hash);
}

export interface CompatResult {
  label: string;
  score: number;
  quote: string;
  pro: string;   // 잘 맞는 점
  con: string;   // 주의할 점
  tip: string;   // 한 줄 팁
}

export function getCompatibility(myType: string, partnerType: string): CompatResult {
  const info = getCompatInfo(myType, partnerType);
  const quotes = COMPAT_QUOTES[info.group];
  const pros = COMPAT_PROS[info.group];
  const cons = COMPAT_CONS[info.group];
  const tips = COMPAT_TIPS[info.group];

  // 날짜 + 두 타입의 조합으로 매일 다른 문구
  const d = new Date();
  const dateStr = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`;
  const pairKey = [myType, partnerType].sort().join('-');
  const seed = getDateSeed(dateStr + pairKey);

  const quoteIdx = seed % quotes.length;
  // 각기 다른 오프셋으로 pro/con/tip도 매일 다르게
  const proIdx = getDateSeed(dateStr + pairKey + 'pro') % pros.length;
  const conIdx = getDateSeed(dateStr + pairKey + 'con') % cons.length;
  const tipIdx = getDateSeed(dateStr + pairKey + 'tip') % tips.length;

  return {
    label: info.label,
    score: info.score,
    quote: quotes[quoteIdx] ?? quotes[0]!,
    pro: pros[proIdx] ?? pros[0]!,
    con: cons[conIdx] ?? cons[0]!,
    tip: tips[tipIdx] ?? tips[0]!,
  };
}
